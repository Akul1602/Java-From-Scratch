package Basics;

public class updatingvariables {
    public static void main(String[] args) {
        int x;
        x = 8;
        System.out.println(x);
        x = 10;
        System.out.println(x);
        x = x + 10;
        System.out.println(x);
        x = 12;
        System.out.println(x);
        x = x + 8;
        System.out.println(x);
    }
}
